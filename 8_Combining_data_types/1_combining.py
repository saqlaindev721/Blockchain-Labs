
# # Concatenate strings
full_name = "Alice" + " " + "Johnson"
print(full_name)  # Output: Alice Johnson

# Add integers
total_apples = 5 + 7
print(total_apples)  # Output: 12

# Add float and integer
total_weight = 5.5 + 2
print(total_weight)  # Output: 7.5

# Boolean operations
is_warm = True
is_rainy = False
print(is_warm and not is_rainy)  # Output: True



