# Create a set
numbers = {1, 2, 3, 4}

# Add an element
numbers.add(5)

# Remove an element
numbers.discard(3)

# Check membership
print(2 in numbers)  # Output: True

print(numbers)  # Output: {1, 2, 4, 5}



