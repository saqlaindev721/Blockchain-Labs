
# Create a dictionary
person = {"name": "Alice", "age": 30}

# Access a value
print(person["name"])  # Output: Alice

# Add a key-value pair
person["city"] = "New York"

# Remove a key-value pair
del person["age"]

print(person)  # Output: {'name': 'Alice', 'city': 'New York'}





