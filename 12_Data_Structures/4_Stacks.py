


# 4. Stacks

# Description: A stack is a LIFO (Last In, First Out) data structure. Elements are added and removed from the top.

# Example:

# python

# Create a stack
stack = []

# Push elements
stack.append('a')
stack.append('b')
stack.append('c')

# Pop elements
print(stack.pop())  # Output: c
print(stack.pop())  # Output: b

# Print remaining elements
print(stack)  # Output: ['a']






