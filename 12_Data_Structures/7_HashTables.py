
# 6. Hash Tables

# Description: A hash table (or hash map) is a data structure that maps keys to values using a hash function to compute an index.

# Example (Using Python's dict):

# python

# Create a hash table (dictionary)
hash_table = {}

# Insert key-value pairs
hash_table['name'] = 'Alice'
hash_table['age'] = 30

# Access values
print(hash_table['name'])  # Output: Alice

# Remove a key-value pair
del hash_table['age']

# Print the hash table
print(hash_table)  # Output: {'name': 'Alice'}















