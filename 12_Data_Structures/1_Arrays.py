# 1. Arrays

# Description: Arrays are collections of elements, all of the same type, stored in contiguous memory locations. Python's built-in list type can be used as an array.

# Example:

# python

# Create an array (list in Python)
arr = [1, 2, 3, 4, 5]

# Access elements
print(arr[0])  # Output: 1

# Modify elements
arr[2] = 10
print(arr)  # Output: [1, 2, 10, 4, 5]

# Iterate over elements
for element in arr:
    print(element)





