
# 8. Recursion

# Description: Recursion is a technique where a function calls itself in order to solve smaller instances of the same problem.

# Example (Factorial Calculation):

# python

def factorial(n):
    if n == 0:
        return 1
    else:
        return n * factorial(n-1)

# Example usage
print(factorial(5))  # Output: 120








