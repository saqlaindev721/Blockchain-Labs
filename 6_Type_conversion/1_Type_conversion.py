# Convert float to integer
height = 5.6
height_int = int(height)
print(height_int)  # Output: 5




# Convert integer to string
age = 30
age_str = str(age)
print(age_str)     # Output: '30'



# Convert string to float
temperature_str = "23.5"
temperature_float = float(temperature_str)
print(temperature_float)  # Output: 23.5



