# Object-Oriented Programming (OOP) in Python is a paradigm that uses "objects" to represent data and methods to manipulate that data. This approach helps in organizing and managing code efficiently. Here’s a comprehensive overview of OOP concepts in Python with examples.
# 1. Classes

# Description: A class is a blueprint for creating objects. It defines a set of attributes (data) and methods (functions) that the objects created from the class can use.

# Example:

# python

class Dog:
    def __init__(self, name, age):
        self.name = name  # Attribute
        self.age = age    # Attribute

    def bark(self):      # Method
        print(f"{self.name} is barking!")

# Creating an object of the class
my_dog = Dog("Buddy", 3)

# Accessing attributes and methods
print(my_dog.name)  # Output: Buddy
print(my_dog.age)   # Output: 3
my_dog.bark()       # Output: Buddy is barking!















