# 3. Sets
# Definition: Unordered, mutable, and does not allow duplicate elements.
# Syntax: my_set = {1, 2, 3, 4, 5}
# Usage: When you need a collection of unique items and the order does not matter.
# Common Operations:

# Adding elements: my_set.add(6)
# Removing elements: my_set.remove(3)
# Membership test: 3 in my_set (returns True)
# Set operations (union, intersection, etc.):
# Union: set1 | set2
# Intersection: set1 & set2
# Difference: set1 - set2