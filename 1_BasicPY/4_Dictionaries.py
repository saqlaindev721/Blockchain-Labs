# Syntax: my_dict = {'key1': 'value1', 'key2': 'value2'}
# Usage: When you need to associate unique keys with values and perform lookups based on those keys.
# Common Operations:
 
# Adding or updating entries: my_dict['key3'] = 'value3'
# Removing entries: del my_dict['key1']
# Accessing values: my_dict['key2'] (returns 'value2')


