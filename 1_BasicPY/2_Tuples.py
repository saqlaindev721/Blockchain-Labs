# 2. --------------Tuples------------------------------
# Definition: Ordered, immutable (unchangeable), and allows duplicate elements.
# Syntax: my_tuple = (1, 2, 3, 4, 5)
# Usage: When you need a collection of items that should not be changed after creation, and where the order matters.
# Common Operations:

# Accessing elements: my_tuple[0] (returns 1)
# Slicing: my_tuple[1:3] (returns (2, 3))
# Tuples cannot be modified after creation.